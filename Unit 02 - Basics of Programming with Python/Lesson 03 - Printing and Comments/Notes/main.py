#This is the print function.  It prints the literal string in single or double quotes
print("Hello World")
print("Hi Again")
#You can separate words by a comma.  A space appear automatically.
print("Hi", "Again")
#You can change the word separator by adding sep=""
print("Hi", "Again", sep="-")
#You can change the ending part by end.  Defaults to a new line.  
print("Hi", "Again", end="")
print("Something")
print("print")
#Single quotes are allowed
print('single quotes')
#Use the \ as the escape character
#prints a single quote
print('\'')
#prints a double quote
print("\"")
#prints 1 backslash
print("\\")
#prints 2 backslashes.
print("\\\\")
#print a tab
print("\tHello")
#prints a new line
print("\n")
print("Hello")

